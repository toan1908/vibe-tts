// Get country that best matches a locale
export function getCountryByLocale(locale) {
  if (!locale) return null;
  
  // First try exact match
  let country = countries.find(c => c.locale.toLowerCase() === locale.toLowerCase());
  if (country) return country;
  
  // Try matching by language + country code
  const parts = locale.split('-');
  if (parts.length >= 2) {
    country = countries.find(c => c.code === parts[1].toUpperCase());
    if (country) return country;
  }
  
  // Try matching by language code only
  country = countries.find(c => c.locale.split('-')[0].toLowerCase() === parts[0].toLowerCase());
  return country;
}

// Get unique countries that have voices
export function getAvailableCountries(voices) {
  const countryMap = new Map();
  
  voices.forEach(v => {
    const localeParts = v.locale ? v.locale.split('-') : v.shortName.split('-');
    const langCode = localeParts[0];
    const countryCode = localeParts[1] || langCode;
    
    const country = countries.find(c => c.locale === v.locale || c.code === countryCode.toUpperCase());
    if (country && !countryMap.has(country.code)) {
      countryMap.set(country.code, { ...country, voiceCount: 0 });
    }
    if (country && countryMap.has(country.code)) {
      countryMap.get(country.code).voiceCount++;
    }
  });
  
  // Sort: Vietnam first, then by voice count
  const result = Array.from(countryMap.values());
  result.sort((a, b) => {
    if (a.code === 'VN') return -1;
    if (b.code === 'VN') return 1;
    return b.voiceCount - a.voiceCount;
  });
  
  return result;
}
